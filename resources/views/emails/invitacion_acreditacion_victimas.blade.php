Antes de explicar en mayor detalle qué derechos se desprenden de esta acreditación y cómo actuar ante la JEP, permítanos exponer una información básica sobre el Caso al que se le invita a participar.

 

#Sobre el Caso No. 001 tramitado por la Sala de Reconocimiento de Verdad y Responsabilidad

 

La Sala de Reconocimiento de la JEP avocó conocimiento del Caso No. 001 el 4 de julio de 2018 a partir del Informe No. 2 presentado por la Fiscalía General de la Nación, denominado “Retención ilegal de personas por parte de las FARC-EP”. El 13 de julio de 2018, la Sala notificó en diligencia pública el inicio del Caso a 31 miembros del Estado Mayor Central de la antigua guerrilla de las FARC-EP y decretó abierta la etapa de “reconocimiento de verdad, de responsabilidad y de determinación de los hechos y las conductas”[1]. Actualmente, la Sala se encuentra adelantando las diligencias de versiones voluntarias de los ex miembros del Estado Mayor de las FARC, siendo este el primer momento de aporte a la verdad. 

 

De acuerdo con las normas que rigen la Jurisdicción Especial para la Paz, la Sala debe desarrollar un proceso de construcción dialógica de la verdad, en donde las víctimas están en el centro. Esto significa que la Sala debe escuchar a las víctimas y a los comparecientes y debe contrastar las diferentes versiones de lo sucedido y los daños ocasionados, a la luz de otras fuentes de información para determinar la verdad. En el marco de este proceso, los comparecientes - ex miembros de las FARC-EP implicados en conductas de retenciones ilegales- deben reconocer su responsabilidad frente a los crímenes cometidos y aportar a la verdad, garantizando los derechos de las víctimas a la verdad, a la reparación y a la no repetición. De no hacerlo, se aplicarán consecuencias relacionadas con la pérdida de beneficios del Sistema Integral de Verdad, Justicia, Reparación y No Repetición de acuerdo a lo establecido en la ley y en el Acuerdo Final de Paz[2].

 

#Sobre los derechos de participación derivados de la acreditación como víctima en el Caso No. 001


Las víctimas acreditadas en el marco de los procedimientos ante la JEP adquieren los derechos de un interviniente especial, según los estándares nacionales e internacionales sobre garantías procesales, sustanciales, probatorias, acceso a un recurso judicial efectivo y demás derechos aplicables. Los artículos 14, 15 y 17 del Proyecto de Ley Estatutaria de la JEP consagran los derechos de las víctimas acreditadas ante la JEP. Entre otros, las víctimas pueden:

 

1.     Aportar pruebas e interponer recursos contra las sentencias que se profieran.

2.     Recibir asesoría, orientación y representación judicial a través del Sistema Autónomo de Asesoría y Defensa (SAAD).

3.     Contar con acompañamiento psicosocial y jurídico en los procedimientos.

4.     Ser informadas del avance de la investigación y del proceso, y de cuándo se llevarán a cabo las distintas audiencias del proceso, e intervenir en ellas.

5.     Solicitar medidas de protección en el caso que sus derechos fundamentales se encontrasen amenazados por su participación en el proceso ante la JEP.

 

Concretamente, ante la Sala de Reconocimiento de Verdad y Responsabilidad, que es la instancia que actualmente conoce del Caso No. 001, las víctimas están facultadas para:[3]

 

1.    Aportar pruebas y, con posterioridad a la recepción de versiones voluntarias, presentar observaciones a estas y solicitar y recibir copia del expediente.

2.    Asistir a la audiencia pública de reconocimiento, de haberla, y dentro de los 15 días hábiles posteriores a la audiencia, presentar observaciones finales escritas sobre todos los aspectos concernientes a la Resolución de Conclusiones.

3.    Presentar observaciones en relación con los proyectos restaurativos propuestos por la persona compareciente.

4.    Las víctimas de violencia basada en género, incluyendo aquellas de violencia sexual, tienen derecho a no ser confrontadas con su agresor.

5.    Interponer recursos en contra de la decisión sobre selección de casos.

6.    Formular preguntas frente a sus hechos victimizantes.

 

#¿Cómo se obtiene la acreditación en el Caso No. 001?

 

De acuerdo con el artículo 3 de la Ley 1922 de 2018, el reconocimiento de la calidad de interviniente especial requiere:

i)              una manifestación expresa de la voluntad en ser acreditado como víctima;

ii)             una prueba sumaria que demuestre su calidad de víctima. Este requisito puede cumplirse, por ejemplo, con la presentación del certificado de inscripción en el Registro Único de Víctimas; el documento que reconoce su calidad de víctima ante Justicia y Paz; una denuncia por los hechos victimizantes efectuada ante otra institución; una sentencia judicial o cualquier otro medio de prueba pertinente o conducente que le permita a la Sala comprobar que fue víctima de los hechos alegados, y

iii)           un relato de los hechos victimizantes, especificando al menos época y lugar de los mismos.

Si los miembros de su familia o allegados padecieron un daño por los hechos victimizantes de la retención ilegal, podrán solicitar su acreditación, aportando un medio de prueba que demuestre el vínculo familiar (v.gr registro civil) o prueba del interés directo y legítimo en ser reconocidos como víctimas, esto es, prueba del daño sufrido.

 

#¿Cómo puede ser representado en el proceso ante la JEP?

 

La ley prevé la posibilidad de participar en el proceso por sí mismo, por medio abogado de confianza, por abogado designado por su organización de víctimas (si existe), o por medio de representante común designado por el Sistema Autónomo de Asesoría y Defensa administrado por la Secretaría Ejecutiva de la JEP, siempre que tenga su consentimiento.

 

En caso de tomar la decisión de ser representado por apoderado de confianza, resulta necesario allegar un poder especial para actuar ante la JEP. En caso de desear ser representado por el abogado común asignado por la JEP, favor indicarlo en el formato de solicitud de acreditación que se adjunta a este correo.

 

Valoramos la confianza que deposita en nosotros al decidir acudir a esta entidad. La acreditación de las víctimas busca materializar el principio de centralidad de las víctimas que orienta todas las actuaciones de la Jurisdicción Especial para la Paz. 

 

