En atención a su condición de víctima reconocida en el proceso de Justicia y Paz por el delito de secuestro cometido por la extinta guerrilla de las FARC-EP, la Sala de Reconocimiento, de Verdad, de Responsabilidad y de Determinación de Hechos y Conductas de la Jurisdicción Especial para la Paz (JEP) le extiende una invitación para acreditarse como víctima en el Caso No. 001 denominado “Retención ilegal de personas por parte de las FARC-EP”.

 

La acreditación le otorgará la calidad de interviniente especial en el proceso ante la JEP, y con ello, su derecho a participar en todas las instancias del Caso 001 que se adelanta frente a los ex miembros de las FARC-EP. 

