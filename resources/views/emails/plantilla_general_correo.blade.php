Estimado señor(a),


<BLOCKQUOTE>
Reciba un cordial saludo por parte de la Jurisdicción Especial para la Paz (JEP).
</BLOCKQUOTE>
<BLOCKQUOTE>
En tanto víctima acreditada en el Caso 01 denominado “Retenciones ilegales por parte de las FARC-EP”, de la Sala de Reconocimiento de Verdad, Responsabilidad y Determinación de los Hechos y Conductas, y en ejercicio de los derechos que tiene como interviniente especial, <b>usted podrá conocer el contenido de las versiones voluntarias rendidas por los excombatientes de la antigua guerrilla de las FARC-EP en el marco del Caso y presentar sus observaciones a las mismas.</b>
</BLOCKQUOTE>
<BLOCKQUOTE>
A través del Auto del 22 de noviembre de 2019, se decretó el inicio de la etapa de traslado de las versiones voluntarias a las víctimas acreditadas para la presentación de estas observaciones y opiniones. Posteriormente, y para garantizar la participación efectiva del conjunto de víctimas acreditadas hasta la fecha, los Magistrados relatores decidieron ampliar el plazo para la presentación de las observaciones hasta el próximo 10 de abril del 2020, por medio del Auto del 17 de marzo de 2020. 
</BLOCKQUOTE>
<BLOCKQUOTE>
Para acceder a esta información de las versiones voluntarias y presentar sus observaciones en línea usted podrá:
<ol>
<li>Acceder al siguiente enlace: <a href="http://abogados.jep.gov.co/traslado/personas">http://abogados.jep.gov.co/traslado/personas</a></li>
<li>Hacer clic en el botón "Recuperar la contraseña".</li>
<li>Ingresar el correo electrónico y documento de identidad que Usted le entregó a la JEP al momento de realizar su solicitud de acreditación como víctima en el Caso 01.</li>
<li>A su correo llegará una solicitud de recuperación de contraseña. Deberá asignar una nueva contraseña que cumpla las siguientes características: deberá tener mínimo 8 caracteres y estos deben tener como mínimo: una letra en mayúscula, un número y un carácter especial (ejemplo: @#$%^&*).</li>
</ol>
</BLOCKQUOTE>
<BLOCKQUOTE>
Una vez ingrese a la plataforma, Usted podrá conocer el contenido de las versiones voluntarias rendidas por los ex combatientes de las extintas FARC-EP que operaron en la zona de ocurrencia de los hechos victimizantes que usted reportó ante la JEP, así como la versión colectiva escrita de las FARC-EP.
</BLOCKQUOTE>
<BLOCKQUOTE>
<b>Por favor lea cuidadosamente la información trasladada y diligencie las preguntas del Formulario en línea dispuesto para la presentación de sus observaciones hasta el 10 de abril 2020.</b>
</BLOCKQUOTE>
<BLOCKQUOTE>
Se adjuntan a esta comunicación el Auto de acreditación del 19 de marzo de 2020 y el Auto del 17 de marzo de 2020, que amplía el plazo para la presentación de observaciones y suspende, entre tanto, la realización de audiencias territoriales.
</BLOCKQUOTE>
